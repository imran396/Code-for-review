namespace Sam\Tax\StackedTax\Schema\Validate\Assignment\Internal\Load;

/**
 * Trait DataProviderCreateTrait
 * @package Sam\Tax\StackedTax\Schema\Validate\Assignment\Internal\Load
 */
trait DataProviderCreateTrait
{
    protected ?DataProvider $dataProvider = null;

    /**
     * @return DataProvider
     */
    protected function createDataProvider(): DataProvider
    {
        return $this->dataProvider ?: DataProvider::new();
    }

    /**
     * @param DataProvider $dataProvider
     * @return $this
     * @internal
     */
    public function setDataProvider(DataProvider $dataProvider): self
    {
        $this->dataProvider = $dataProvider;
        return $this;
    }
}
